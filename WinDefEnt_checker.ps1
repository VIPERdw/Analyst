                # в”Җв”Җ Threat-Aktion в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
                { $_ -in @(1007, 1117) } {
                    $rawPath    = Get-XmlData -Xml $eventXml -FieldName 'Path'
                    $threatName = Get-XmlData -Xml $eventXml -FieldName 'Threat Name'
                    $action     = Get-XmlData -Xml $eventXml -FieldName 'Action Name'
                    if (-not $action) { $action = Get-XmlData -Xml $eventXml -FieldName 'Action ID' }

                    $cleanPath = Extract-ThreatPath -RawPath $rawPath
                    $msg = ""
                    if ($action)     { $msg += "Aktion: $action | " }
                    if ($threatName) { $msg += "[$threatName] " }
                    if ($cleanPath)  { $msg += $cleanPath } else { $msg += "(kein Pfad)" }

                    $isFlagged = Test-CheatMatch -Text "$cleanPath $threatName"
                    if ($isFlagged) { $flagCount++ }
                    Write-Row -Time $timestamp -EventID $id -Type "Threat-Aktion" -Message $msg -Color Red -Flagged:$isFlagged
                }

                # в”Җв”Җ Bereinigung fehlgeschlagen в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
                1008 {
                    $rawPath    = Get-XmlData -Xml $eventXml -FieldName 'Path'
                    $threatName = Get-XmlData -Xml $eventXml -FieldName 'Threat Name'
                    $cleanPath  = Extract-ThreatPath -RawPath $rawPath
                    $msg = "BEREINIGUNG FEHLGESCHLAGEN | "
                    if ($threatName) { $msg += "[$threatName] " }
                    $msg += if ($cleanPath) { $cleanPath } else { "(kein Pfad)" }

                    $isFlagged = Test-CheatMatch -Text "$cleanPath $threatName"
                    if ($isFlagged) { $flagCount++ }
                    Write-Row -Time $timestamp -EventID $id -Type "Berein. FEHLER" -Message $msg -Color DarkRed -Flagged:$isFlagged
                }

                # в”Җв”Җ Bereinigung erfolgreich в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
                1119 {
                    $rawPath    = Get-XmlData -Xml $eventXml -FieldName 'Path'
                    $threatName = Get-XmlData -Xml $eventXml -FieldName 'Threat Name'
                    $cleanPath  = Extract-ThreatPath -RawPath $rawPath
                    $msg = ""
                    if ($threatName) { $msg += "[$threatName] " }
                    $msg += if ($cleanPath) { $cleanPath } else { "(kein Pfad)" }
                    $isFlagged = Test-CheatMatch -Text "$cleanPath $threatName"
                    if ($isFlagged) { $flagCount++ }
                    Write-Row -Time $timestamp -EventID $id -Type "Berein. OK" -Message $msg -Color DarkGreen -Flagged:$isFlagged
                }

                # в”Җв”Җ QuarantГӨne WIEDERHERGESTELLT (sehr verdГӨchtig!) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
                1009 {
                    $rawPath    = Get-XmlData -Xml $eventXml -FieldName 'Path'
                    $threatName = Get-XmlData -Xml $eventXml -FieldName 'Threat Name'
                    $cleanPath  = Extract-ThreatPath -RawPath $rawPath
                    $msg = "AUS QUARANTГ„NE WIEDERHERGESTELLT | "
                    if ($threatName) { $msg += "[$threatName] " }
                    $msg += if ($cleanPath) { $cleanPath } else { "(kein Pfad)" }
                    $isFlagged = Test-CheatMatch -Text "$cleanPath $threatName"
                    if ($isFlagged) { $flagCount++ }
                    # Immer als flagged markieren вҖ“ QuarantГӨne-Restore ist extrem verdГӨchtig
                    Write-Row -Time $timestamp -EventID $id -Type "QUARANT. RESTORE" -Message $msg -Color Yellow -Flagged
                    $flagCount++
                }

                # в”Җв”Җ Verlauf gelГ¶scht (1013) вҖ“ Hinweis auf Verschleierung в”Җв”Җ
                1013 {
                    Write-Row -Time $timestamp -EventID $id -Type "VERLAUF GELГ–SCHT" -Message "Schutzverlauf-Eintrag wurde manuell entfernt!" -Color Yellow -Flagged
                    $flagCount++
                }

                # в”Җв”Җ Fallback: alle anderen geladenen Events в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
                default {
                    $msg = $event.Message
                    if ($msg -and $msg.Length -gt 100) { $msg = $msg.Substring(0,97) + "..." }
                    if (-not $msg) { $msg = "(Kein Nachrichtentext)" }
                    Write-Row -Time $timestamp -EventID $id -Type "Sonstig" -Message $msg -Color DarkGray
                }
            }
        }
        catch {
            Write-Host "  [Fehler bei Event $($event.Id)]: $($_.Exception.Message)" -ForegroundColor DarkGray
        }
    }

    Write-Host ("-" * 90) -ForegroundColor DarkGray
    Write-Host ""
    Write-Host "  Gesamt Events verarbeitet : $($events.Count)" -ForegroundColor Cyan
    if ($flagCount -gt 0) {
        Write-Host "  [!!!] VERDГ„CHTIGE Events    : $flagCount" -ForegroundColor Yellow
        Write-Host "        Hinweis: Geflaggte EintrГӨge deuten auf mГ¶gliche Cheat-AktivitГӨt hin!" -ForegroundColor Yellow
    }
    else {
        Write-Host "  Keine verdГӨchtigen EintrГӨge gefunden." -ForegroundColor Green
    }
}

