function Show-Quarantine {
    Write-Header "QUARANTГ„NE-EINTRГ„GE"

    $mpCmd = "$env:ProgramFiles\Windows Defender\MpCmdRun.exe"
    if (-not (Test-Path $mpCmd)) {
        $mpCmd = "$env:ProgramFiles (x86)\Windows Defender\MpCmdRun.exe"
    }

    if (Test-Path $mpCmd) {
        try {
            $output = & $mpCmd -Scan -ScanType 3 2>&1
            Write-Host "  QuarantГӨne-Abfrage abgeschlossen." -ForegroundColor Cyan
        }
        catch {
            Write-Host "  Fehler beim AusfГјhren von MpCmdRun.exe: $_" -ForegroundColor Red
        }

        # QuarantГӨne-Dateien direkt lesen
        $quarantineDir = "$env:ProgramData\Microsoft\Windows Defender\Quarantine"
        if (Test-Path $quarantineDir) {
            $qFiles = Get-ChildItem -Path $quarantineDir -Recurse -ErrorAction SilentlyContinue
            if ($qFiles) {
                Write-Host "  QuarantГӨne-Ordner enthГӨlt $($qFiles.Count) Dateien/Ordner:" -ForegroundColor DarkYellow
                foreach ($f in $qFiles | Select-Object -First 50) {
                    $isSusp = Test-CheatMatch -Text $f.Name
                    $color  = if ($isSusp) { 'Yellow' } else { 'Gray' }
                    $prefix = if ($isSusp) { "  [!!!] " } else { "        " }
                    Write-Host "${prefix}$($f.FullName) [$($f.LastWriteTime.ToString('yyyy-MM-dd HH:mm'))]" -ForegroundColor $color
                }
            }
            else {
                Write-Host "  QuarantГӨne-Ordner ist leer." -ForegroundColor Green
            }
        }
        else {
            Write-Host "  QuarantГӨne-Verzeichnis nicht zugГӨnglich (Admin?)" -ForegroundColor DarkGray
        }
    }
    else {
        Write-Host "  MpCmdRun.exe nicht gefunden вҖ“ QuarantГӨne-Abfrage nicht mГ¶glich." -ForegroundColor DarkGray
    }
}

