function Write-Header {
    param([string]$Text)
    Write-Host ""
    Write-Host ("=" * 80) -ForegroundColor DarkMagenta
    Write-Host "  $Text" -ForegroundColor Magenta
    Write-Host ("=" * 80) -ForegroundColor DarkMagenta
}

function Write-Row {
    param(
        [string]$Time,
        [string]$EventID,
        [string]$Type,
        [string]$Message,
        [ConsoleColor]$Color = 'White',
        [switch]$Flagged
    )
    $prefix = if ($Flagged) { "[!!!] " } else { "      " }
    $line = "{0}{1,-20} [{2,-4}] {3,-22} {4}" -f $prefix, $Time, $EventID, $Type, $Message
    Write-Host $line -ForegroundColor $Color
    if ($Flagged) {
        Write-Host ("       >>> VERDГ„CHTIG: MГ¶glicher Cheat-Bezug <<<") -ForegroundColor Yellow
    }
}

function Test-CheatMatch {
    param([string]$Text)
    if ([string]::IsNullOrWhiteSpace($Text)) { return $false }
